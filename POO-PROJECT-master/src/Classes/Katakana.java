
package Classes;



public class Katakana
{

    private String palabra;
    private String traduccion;

    public Katakana( String pal, String trad )
    {
        palabra = pal;
        traduccion = trad;
       
    }

    public String darPalabra( )
    {
        return palabra;
    }

    public String darTraduccion( )
    {
        return traduccion;
    }

    
   

  
}
